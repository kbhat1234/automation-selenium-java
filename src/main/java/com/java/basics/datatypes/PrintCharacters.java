package com.java.basics.datatypes;

public class PrintCharacters {

	public static void main(String[] args) {
		System.out.println("++++++++++++");
		System.out.println("@@@@@@@@@@@");
		System.out.println("*************");
		System.out.println("##############");
		
		String a1="+++++++++";
		String a2="@@@@@@@@@@";
		String a3="***********";
		String a4="############";
		System.out.println("a1 string is "+a1);
		System.out.println("a2 string is "+a2);
		System.out.println("a3 string is "+a3);
		System.out.println("a4 string is "+a4);
	}

}
